/**
 * 字符串转整数
 */
public class Solution8 {

    public int myAtoi(String str) {
        char[] chars = str.toCharArray();
        char startChar;
        StringBuilder result = new StringBuilder();
        for (int i = 0; i < chars.length; i++) {
            startChar = chars[i];
            if (startChar == ' ') continue;

        }
        return 0;
    }
}
